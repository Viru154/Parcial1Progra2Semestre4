public class Helicopter {
    private String cambios, rotor, eje, Contvuelo, Helices;

    public String getCambios() {
        return cambios;
    }

    public void setCambios(String cambios) {
        this.cambios = cambios;
    }

    public String getRotor() {
        return rotor;
    }

    public void setRotor(String rotor) {
        this.rotor = rotor;
    }

    public String getEje() {
        return eje;
    }

    public void setEje(String eje) {
        this.eje = eje;
    }

    public String getContvuelo() {
        return Contvuelo;
    }

    public void setContvuelo(String Contvuelo) {
        this.Contvuelo = Contvuelo;
    }

    public String getHelices() {
        return Helices;
    }

    public void setHelices(String Helices) {
        this.Helices = Helices;
    }
    
    
    
}
