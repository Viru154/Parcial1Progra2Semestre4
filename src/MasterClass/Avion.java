public class Avion extends Aereos{
    private String fuselaje, suspencion, control, motores, Pistaaterrizaje;

    public String getFuselaje() {
        return fuselaje;
    }

    public void setFuselaje(String fuselaje) {
        this.fuselaje = fuselaje;
    }

    public String getSuspencion() {
        return suspencion;
    }

    public void setSuspencion(String suspencion) {
        this.suspencion = suspencion;
    }

    public String getControl() {
        return control;
    }

    public void setControl(String control) {
        this.control = control;
    }

    public String getMotores() {
        return motores;
    }

    public void setMotores(String motores) {
        this.motores = motores;
    }

    public String getPistaaterrizaje() {
        return Pistaaterrizaje;
    }

    public void setPistaaterrizaje(String Pistaaterrizaje) {
        this.Pistaaterrizaje = Pistaaterrizaje;
    }
    
    
    
}
